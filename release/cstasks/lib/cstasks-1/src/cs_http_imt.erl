%% This file contain IMT (Internet Media Type) related functions.
%% It is used for detecting proper IMT from files extensions into the 
%% cs_http_handler_www module.
-module(cs_http_imt).
-author("Madalin Grigore-Enescu").

-export([file_type/2]).
-export([type_to_extensions/2]).
-export([extension_to_type/2]).

-spec file_type(File, Default) -> string() | Default when 
    File :: iolist() | binary(),
    Default :: any().
%% @doc Return the IMT for the specified filename. Returns Default parameter if the IMT could not be determined.
file_type(File, Default) ->
	FileExtension = filename:extension(erlang:iolist_to_binary(File)),
	case FileExtension of
		<<".", Extension/binary>> ->
			extension_to_type(Extension, Default);
		_ -> Default
	end.

